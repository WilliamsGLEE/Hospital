public class Dector {

    private int dectorNo;
    private String name;
    private String jobName;

    public int getDectorNo() {
        return dectorNo;
    }

    public void setDectorNo(int dectorNo) {
        this.dectorNo = dectorNo;
    }

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }

    public String getJobName() {
        return jobName;
    }

    public void setJobName(String jobName) {
        this.jobName = jobName;
    }
}
